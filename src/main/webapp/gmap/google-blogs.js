function getBlogsByTag(tag) {
  var blogs = new Array();
  for (var i=0; i<googleBlogs.length; i++) {
    var pbe = googleBlogs[i];
    if (pbe.tags && pbe.tags.length > 0) {
      for (var t=0; t<pbe.tags.length; t++) {
        if (pbe.tags[t].toLowerCase() == tag.toLowerCase() ) {
          blogs.push(pbe);
          break;
        }

      }
    }
  }
  return blogs;
}
